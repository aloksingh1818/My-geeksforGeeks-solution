class Solution {
  public:
    pair<long long, long long> getMinMax(vector<long long int> arr) {
        long long  ans = LLONG_MAX;
        long long  ans2= LLONG_MIN;
        
        for(int i=0;i<arr.size();i++){
           ans= min(arr[i],ans);
           ans2=max(arr[i],ans2);
           
        }
        return { ans,ans2};
    }
};

